namespace kalkulator;

public class Kalkulator
{
    public int AddNumbers(int a, int b)
    {
        return a + b;
    }

    public double AddNumbers(double a, double b)
    {
        return a + b;
    }

    public int SubtractNumbers(int a, int b)
    {
        return a - b;
    }

    public double SubtractNumbers(double a, double b)
    {
        return a - b;
    }

    public int MultiplyNumber(int a, int b)
    {
        return a * b;
    }

    public double MultiplyNumber(double a, double b)
    {
        return a * b;
    }

    public int DivisionNumber(int a, int b)
    {
        return a / b;
    }

    public double DivisionNumber(double a, double b)
    {
        return a / b;
    }
}
